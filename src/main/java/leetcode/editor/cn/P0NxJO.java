    class Solution {
        public int magicTower(int[] nums) {
            Queue<Integer> queue = new PriorityQueue<>();
            long cur = 1, sum = 1;
            int result = 0;
            for (int num : nums) {
                sum += num;
                if (num < 0) {
                    queue.offer(num);
                    cur += num;
                    if (cur <= 0) {
                        cur -= queue.poll();
                        result++;
                    }
                } else {
                    cur += num;
                }
            }
            return sum > 0 ? result : -1;
        }
    }
