    class Solution {
        public int[] dailyTemperatures(int[] T) {
            int[] result = new int[T.length];
            Arrays.fill(result, 0);
            Stack<Integer> stack = new Stack<>();
            for (int i = 0; i < T.length; i++) {
                while (!stack.isEmpty() && T[stack.peek()] < T[i]) {
                    result[stack.peek()] = i - stack.pop();
                }
                stack.push(i);
            }
            return result;
        }
    }
