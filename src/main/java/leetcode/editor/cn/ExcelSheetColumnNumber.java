    class Solution {
        public int titleToNumber(String columnTitle) {
            char[] chs = columnTitle.toCharArray();
            int result = 0;
            int size = chs.length - 1;
            int mul = 1;
            for (int i = 0; i <= size; i++) {
                int num = chs[size - i] - 'A' + 1;
                result += num * mul;
                mul *= 26;
            }
            return result;
        }
    }
