    class Solution {
        public int countArrangement(int n) {
            return dfs(n, 1, new boolean[n]);
        }

        private int dfs(int n, int index, boolean[] use) {
            if (index > n) {
                return 1;
            }
            int count = 0;
            for (int i = 1; i <= n; i++) {
                if (use[i - 1] || (i % index > 0 && index % i > 0)) {
                    continue;
                }
                use[i - 1] = true;
                count += dfs(n, index + 1, use);
                use[i - 1] = false;
            }
            return count;
        }
    }
